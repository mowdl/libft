#include "libft.h"

int	ft_atoi(const char *str)
{
	long	r;
	long	s;

	while (*str != '\0'
		&& (*str == ' ' || *str == '\t' || (*str >= 9 && *str <= 13)))
	{
		str++;
	}
	s = 1;
	if (*str == '-' || *str == '+')
	{
		if (*str == '-')
			s = -1;
		str++;
	}
	r = 0;
	while (*str && ft_isdigit(*str))
	{
		r = r * 10 + (*str - '0');
		str++;
	}
	return (r * s);
}
