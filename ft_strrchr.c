char	*ft_strrchr(const char *s, int c)
{
	char	*r;
	char	*buf;

	buf = (char *)s;
	r = 0;
	while (*buf != '\0')
	{
		if (*buf == c)
			r = buf;
		buf++;
	}
	if (c == '\0')
		return (buf);
	return (r);
}
